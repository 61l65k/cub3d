#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = (t_list *)malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->content = content;
	node->ambiguous_redirect = false;
	node->type = IO_NONE;
	node->next = NULL;
	return (node);
}
